package com.wokun.tysl.model.bean;

public class ProfitLog {

    private String profit_source_type;
    private String profit_money;
    private String log_time;

    public String getProfit_source_type() {
        return profit_source_type;
    }

    public void setProfit_source_type(String profit_source_type) {
        this.profit_source_type = profit_source_type;
    }

    public String getProfit_money() {
        return profit_money;
    }

    public void setProfit_money(String profit_money) {
        this.profit_money = profit_money;
    }

    public String getLog_time() {
        return log_time;
    }

    public void setLog_time(String log_time) {
        this.log_time = log_time;
    }
}
