package com.wokun.tysl.smartretail.bean;

public class IdCartInfoBean {

    private String userName;
    private String idCartName;
    private String idCartNumber;

    public String getUserName() {
        return userName;
    }

    public String getIdCartName() {
        return idCartName;
    }

    public String getIdCartNumber() {
        return idCartNumber;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setIdCartName(String idCartName) {
        this.idCartName = idCartName;
    }

    public void setIdCartNumber(String idCartNumber) {
        this.idCartNumber = idCartNumber;
    }
}
